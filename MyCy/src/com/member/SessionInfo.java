package com.member;

// 세션에 저장할 정보를 가진 클래스
public class SessionInfo {
	
	private String id, name;
	
	public String getId() {
		return id;
	}
	
	public void setId(String id) {
		this.id = id;		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
